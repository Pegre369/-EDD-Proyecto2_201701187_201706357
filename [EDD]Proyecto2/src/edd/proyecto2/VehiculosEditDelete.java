        public void rellenar(ArbolB nodo){
        if(nodo.values.size()>0){
            for (Vehiculo ve : nodo.values) {
                cbxPlacas.addItem(ve.getPlaca());
            }
            for(ArbolB child : nodo.children) {
                if(child.values.size()>0){
                    rellenar(child);
                }
                
            }
        }
    }
