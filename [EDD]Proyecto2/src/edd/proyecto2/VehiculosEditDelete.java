    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String placaId= (String) cbxPlacas.getSelectedItem();
        Vehiculo temp = Pantalla_Principal.vehiculoRoot.searchPlaca(Pantalla_Principal.vehiculoRoot, placaId);
        if(temp!=null){
            txtPlaca.setText(temp.getPlaca());
            txtMarca.setText(temp.getMarca());
            txtModelo.setText(temp.getModelo());
            txtAnio.setText( String.valueOf(temp.getAnio()));
            txtColor.setText(temp.getColor());
            txtPrecio.setText(temp.getPrecio());
            txtTransmision.setText(temp.getTransmision());
            
        }else{
            System.out.println("Placa no encontrada");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String placaId= txtPlaca.getText();
        Vehiculo temp = Pantalla_Principal.vehiculoRoot.searchPlaca(Pantalla_Principal.vehiculoRoot, placaId);
        if(temp!=null){
            temp.setAnio( Integer.valueOf(txtAnio.getText()));
            temp.setColor(txtColor.getText());
            temp.setMarca(txtMarca.getText());
            temp.setModelo(txtModelo.getText());
            temp.setPrecio(txtPrecio.getText());
            temp.setTransmision(txtTransmision.getText());
            
            JOptionPane.showMessageDialog(null, "Editado con exite ");
        }else{
            JOptionPane.showMessageDialog(null, "Placa no existe");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String placaId= txtPlaca.getText();
        Vehiculo temp = Pantalla_Principal.vehiculoRoot.searchPlaca(Pantalla_Principal.vehiculoRoot, placaId);
        if(temp!=null){
            Pantalla_Principal.vehiculoRoot= Pantalla_Principal.vehiculoRoot.delete(temp);
            JOptionPane.showMessageDialog(null, "Elimado con exite ");
        }else{
            JOptionPane.showMessageDialog(null, "Placa no existe");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

